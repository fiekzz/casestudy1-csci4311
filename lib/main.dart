import 'package:casestudy1/login/user_login.dart';
import 'package:casestudy1/utils/pgettoken.dart';
import 'package:flutter/material.dart';
import 'package:flutter_secure_storage/flutter_secure_storage.dart';

void main() {
  runApp(const MyApp());
}

class MyApp extends StatefulWidget {
  const MyApp({super.key});

  @override
  State<MyApp> createState() => _MyAppState();
}

final navigatorKey = GlobalKey<NavigatorState>();
final scaffoldMessengerKey = GlobalKey<ScaffoldMessengerState>();

class _MyAppState extends State<MyApp> {
  final storage = FlutterSecureStorage();

  @override
  void initState() {
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      navigatorKey: navigatorKey,
      scaffoldMessengerKey: scaffoldMessengerKey,
      title: 'Payroll Management System',
      theme: ThemeData(
        colorScheme: ColorScheme.fromSeed(seedColor: Colors.deepPurple),
        useMaterial3: true,
      ),
      debugShowCheckedModeBanner: false,
      // home: userToken != null
      //     ? UserDashboard(userID: userToken!.sub!)
      //     : const UserLoginScreen(),
      home: FutureBuilder(
        future: Future.wait([storage.readAll()]),
        builder: (context, snapshot) {
          if (snapshot.connectionState == ConnectionState.waiting) {
            return const Scaffold(
              body: Center(
                child: CircularProgressIndicator(),
              ),
            );
          }

          if (snapshot.connectionState == ConnectionState.done) {
            final secureStorage = snapshot.data?[0];

            if (secureStorage != null) {
              final token = secureStorage['token'];

              if (token != null) {
                final userToken = PGetToken.tokenParser(token);

                return UserDashboard(userID: userToken.sub!);
              }
            }

            return const UserLoginScreen();
          }

          return const SizedBox();
        },
      ),
    );
  }
}

